    private void Llenarlista() {

        String Cabecera[] = {"Id Personas", "Nombre", "Apellido", "Direccion", "Telefono", "Email", "Rol", "Fecha Nacimiento", "Id NFC", "Numero Ficha"};
        DefaultTableModel dtm = new DefaultTableModel(null, Cabecera);
        jTable1.setModel(dtm);
        try {
            LinkedList<Personas> list = pers.ListarPersonas(frmSplash.con);
            Iterator ilist = list.iterator();
            String fila[] = new String[10];
            while (ilist.hasNext()) {
                Personas pers = (Personas) ilist.next();
                fila[0] = pers.getIdPersonas();
                fila[1] = pers.getNombre();
                fila[2] = pers.getApellido();
                fila[3] = pers.getDireccion();
                fila[4] = pers.getTelefono();
                fila[5] = pers.getEmail();
                switch (pers.getRol_idrol()) {
                    case 1:
                        fila[6] = "ADMINISTRADOR";
                        break;
                    case 2:
                        fila[6] = "VIGILANTE";
                        break;
                    case 3:
                        fila[6] = "INSTRUCTOR";
                        break;

                    case 4:
                        fila[6] = "APRENDIZ";
                        break;
                    case 5:
                        fila[6] = "VISITANTE";
                        break;
                }
                fila[7] = pers.getFecha_nacimiento();
                fila[8] = String.valueOf(pers.getNFC_id());
                fila[9] = pers.getNumero_ficha();

                dtm = (DefaultTableModel) jTable1.getModel();
                dtm.addRow(fila);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

        ValidarDatosTabla();

    }

