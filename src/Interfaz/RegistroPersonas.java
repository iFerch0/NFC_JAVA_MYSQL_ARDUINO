    public void filtro() {
        String opcion = jcbOpcion.getSelectedItem().toString();

        if (opcion == "Id Personas") {
            filtro = jTextField1.getText();
            trsfiltro.setRowFilter(RowFilter.regexFilter(jTextField1.getText(), 0));
        }
        if (opcion == "Nombre") {
            filtro = jTextField1.getText();
            trsfiltro.setRowFilter(RowFilter.regexFilter(jTextField1.getText(), 1));
        }
        if (opcion == "Apellido") {
            filtro = jTextField1.getText();
            trsfiltro.setRowFilter(RowFilter.regexFilter(jTextField1.getText(), 2));
        }
        if (opcion == "Direccion") {
            filtro = jTextField1.getText();
            trsfiltro.setRowFilter(RowFilter.regexFilter(jTextField1.getText(), 3));
        }
        if (opcion == "Telefono") {
            filtro = jTextField1.getText();
            trsfiltro.setRowFilter(RowFilter.regexFilter(jTextField1.getText(), 4));
        }
        if (opcion == "Email") {
            filtro = jTextField1.getText();
            trsfiltro.setRowFilter(RowFilter.regexFilter(jTextField1.getText(), 5));
        }
        if (opcion == "Rol") {
            filtro = jTextField1.getText();
            trsfiltro.setRowFilter(RowFilter.regexFilter(jTextField1.getText(), 6));
        }
        if (opcion == "Fecha Nacimiento") {
            filtro = jTextField1.getText();
            trsfiltro.setRowFilter(RowFilter.regexFilter(jTextField1.getText(), 7));
        }
        if (opcion == "Id NFC") {
            filtro = jTextField1.getText();
            trsfiltro.setRowFilter(RowFilter.regexFilter(jTextField1.getText(), 8));
        }
        if (opcion == "Numero Ficha") {
            filtro = jTextField1.getText();
            trsfiltro.setRowFilter(RowFilter.regexFilter(jTextField1.getText(), 9));
        }

    }
